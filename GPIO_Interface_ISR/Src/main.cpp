#include <bitset>
#include <gpio_hal.h>
#include <IGpio.h>
#include "PortB.h"
#include "PortC.h"
#include "main.h"
#include "string.h"

void SystemClock_Config(void);

stm32f767::gpio::IGpio * igpio;
stm32f767::gpio::PortB portB;
stm32f767::gpio::PortC portC(stm32f767::gpio::Settings(GPIO_MODE_IT_RISING,GPIO_NOPULL,GPIO_SPEED_FREQ_VERY_HIGH,0u));

int main(void)
{
  igpio = &portB;

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  igpio->Initialize(GPIO_PIN_7);

  igpio = &portC;
  igpio->Initialize(GPIO_PIN_13);

  /* Enable and set EXTI line 15_10 Interrupt to the lowest priority */
  HAL_NVIC_SetPriority(EXTI15_10_IRQn, 2, 0);
  HAL_NVIC_EnableIRQ(EXTI15_10_IRQn);

  igpio = &portB;


  while (1)
  {
    HAL_Delay(100);
  }

}

